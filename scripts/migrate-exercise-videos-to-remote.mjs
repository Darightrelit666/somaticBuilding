const nameSimilarityScore = (left, right) => {
  const a = normalizeNameKey(left);
  const b = normalizeNameKey(right);
  if (!a || !b) return 0;
  if (a === b) return 1;
  if (a.includes(b) || b.includes(a)) return 0.93;

  const aTokens = tokenSet(a);
  const bTokens = tokenSet(b);
  if (aTokens.size === 0 || bTokens.size === 0) return 0;

  let intersection = 0;
  for (const token of aTokens) {
    if (bTokens.has(token)) intersection += 1;
  }

  const union = aTokens.size + bTokens.size - intersection;
  const jaccard = union > 0 ? intersection / union : 0;
  const containment = intersection / Math.min(aTokens.size, bTokens.size);
  return Number((jaccard * 0.7 + containment * 0.3).toFixed(4));
};

const canonicalSimilarityScore = (left, right) => {
  const aTokens = tokenSet(left);
  const bTokens = tokenSet(right);
  if (aTokens.size === 0 || bTokens.size === 0) return 0;
  if (canonicalNameKey(left) === canonicalNameKey(right)) return 1;

  let intersection = 0;
  for (const token of aTokens) {
    if (bTokens.has(token)) intersection += 1;
  }
  const union = aTokens.size + bTokens.size - intersection;
  const jaccard = union > 0 ? intersection / union : 0;
  const containment = intersection / Math.min(aTokens.size, bTokens.size);
  return Number((jaccard * 0.6 + containment * 0.4).toFixed(4));
};

const shouldAcceptExerciseDbMatch = (targetName, candidateName) => {
  const rawScore = nameSimilarityScore(targetName, candidateName);
  const canonScore = canonicalSimilarityScore(targetName, candidateName);
  const score = Math.max(rawScore, canonScore);
  if (!strictMatch) {
    return score >= 0.58;
  }
  const targetTokenCount = tokenSet(targetName).size;
  if (targetTokenCount <= 2) {
    return score >= 0.72;
  }
  if (targetTokenCount <= 4) {
    return score >= 0.68;
  }
  return score >= 0.64;
};

const runMysql = (sql) =>
  new Promise((resolve, reject) => {
    const mysqlArgs = [
      "-h",
      dbConfig.host,
      "-P",
      String(dbConfig.port),
      "-u",
      dbConfig.user,
      `-p${dbConfig.password}`,
      "-D",
      dbConfig.database,
      "--default-character-set=utf8mb4",
      "-N",
      "-B"
    ];
    const child = spawn("mysql", mysqlArgs, {
      stdio: ["pipe", "pipe", "pipe"]
    });
    let stdout = "";
    let stderr = "";
    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });
    child.stderr.on("data", (chunk) => {
      stderr += chunk.toString();
    });
    child.on("error", (error) => reject(error));
    child.on("close", (code) => {
      if (code === 0) {
        resolve({ stdout, stderr });
      } else {
        reject(new Error(`mysql exited with code ${code}: ${stderr || stdout}`));
      }
    });
    child.stdin.write(sql);
    child.stdin.end();
  });

const fetchJsonWithRetry = async (url, attempts = 6, timeoutMs = 20000) => {
  let lastError;
  for (let i = 1; i <= attempts; i += 1) {
    try {
      const controller = new AbortController();
      const timer = setTimeout(() => controller.abort(), timeoutMs);
      const response = await fetch(url, {
        headers: {
          Accept: "application/json",
          "User-Agent": "somaticbuilding-remote-video-migrator"
        },
        signal: controller.signal
      });
      clearTimeout(timer);
      if (!response.ok) {
        const error = new Error(`HTTP ${response.status}`);
        error.status = response.status;
        throw error;
      }
      return response.json();
    } catch (error) {
      lastError = error;
      if (i < attempts) {
        const status = Number(error?.status ?? 0);
        const baseDelay =
          status === 429 || status === 503
            ? 2500 * i
            : error?.name === "AbortError"
            ? 1800 * i
            : 900 * i;
        await sleep(baseDelay);
      }
    }
  }
  throw lastError;
};

const readLocalExercises = async () => {
  const sql = `
SELECT
  e.id,
  e.name,
  COALESCE(
    (
      SELECT m.url
      FROM exercise_media m
      WHERE m.exercise_id = e.id
        AND m.media_type = 'video'
        AND m.is_deleted = 0
      ORDER BY m.id DESC
      LIMIT 1
    ),
    ''
  ) AS current_video_url
FROM exercise e
WHERE is_deleted = 0
ORDER BY e.id;
`;
  const result = await runMysql(sql);
  const lines = result.stdout
    .split(/\r?\n/g)
    .map((line) => line.trim())
    .filter(Boolean);

  return lines
    .map((line) => {
      const [idRaw, nameRaw, currentVideoRaw] = line.split("\t");
      return {
        id: Number(idRaw),
        name: nameRaw ?? "",
        currentVideoUrl: currentVideoRaw ?? ""
      };
    })
    .filter((row) => Number.isFinite(row.id) && row.id > 0 && row.name.trim().length > 0);
};

const toAbsoluteWgerUrl = (url) => {
  const safeUrl = String(url ?? "").trim();
  if (!safeUrl) return "";
  if (safeUrl.startsWith("http://") || safeUrl.startsWith("https://")) return safeUrl;
  if (safeUrl.startsWith("/")) return `https://wger.de${safeUrl}`;
  return "";
};

const isSafeRemoteMediaUrl = (url) => {
  const safeUrl = String(url ?? "").trim();
  if (!safeUrl) return false;
  if (isLikelyYoutubeUrl(safeUrl)) return false;
  let parsed;
  try {
    parsed = new URL(safeUrl);
  } catch {
    return false;
  }
  if (!(parsed.protocol === "https:" || parsed.protocol === "http:")) return false;

  const host = parsed.hostname.toLowerCase();
  const pathName = parsed.pathname.toLowerCase();
  const isWgerHost = host === "wger.de" || host.endsWith(".wger.de");
  const isExerciseDbHost = host === "static.exercisedb.dev";
  const hasPlayableExt = /\.(mp4|webm|mov|m4v|gif)$/i.test(pathName);

  if (isExerciseDbHost) {
    return /\.gif$/i.test(pathName);
  }
  if (isWgerHost) {
    return hasPlayableExt;
  }
  return false;
};

const loadWgerVideos = async () => {
  const map = new Map();
  let nextUrl = "https://wger.de/api/v2/exerciseinfo/?language=2&limit=200";
  let page = 0;
  while (nextUrl) {
    page += 1;
    const payload = await fetchJsonWithRetry(nextUrl);
    const list = Array.isArray(payload?.results) ? payload.results : [];
    for (const item of list) {
      const videos = Array.isArray(item?.videos) ? item.videos : [];
      const videoUrl = videos
        .map((entry) => (typeof entry?.video === "string" ? toAbsoluteWgerUrl(entry.video) : ""))
        .find((entry) => isSafeRemoteMediaUrl(entry));
      if (!videoUrl) continue;

      const translations = Array.isArray(item?.translations) ? item.translations : [];
      const aliases = new Set();
      if (typeof item?.name === "string" && item.name.trim()) {
        aliases.add(item.name.trim());
      }
      for (const translation of translations) {
        if (typeof translation?.name === "string" && translation.name.trim()) {
          aliases.add(translation.name.trim());
        }
        if (Array.isArray(translation?.aliases)) {
          for (const alias of translation.aliases) {
            if (typeof alias === "string" && alias.trim()) {
              aliases.add(alias.trim());
            }
          }
        }
      }

      for (const alias of aliases) {
        const key = normalizeNameKey(alias);
        if (!key) continue;
        if (!map.has(key)) {
          map.set(key, {
            url: videoUrl.trim(),
            source: "wger",
            title: alias
          });
        }
      }
    }

    console.log(`wger page=${page}, mapKeys=${map.size}`);
    nextUrl = typeof payload?.next === "string" && payload.next.trim() ? payload.next.trim() : "";
  }
  return map;
};

const readExerciseDbIndexCache = async () => {
  try {
    const raw = await fs.readFile(exerciseDbIndexCachePath, "utf8");
    const payload = JSON.parse(raw);
    if (!Array.isArray(payload?.entries)) {
      return null;
    }
    return payload.entries
      .map((item) => ({
        name: typeof item?.name === "string" ? item.name.trim() : "",
        gifUrl: typeof item?.gifUrl === "string" ? item.gifUrl.trim() : ""
      }))
      .filter((item) => item.name && isSafeRemoteMediaUrl(item.gifUrl));
  } catch {
    return null;
  }
};

const loadExerciseDbIndex = async () => {
  const cachedEntries = await readExerciseDbIndexCache();
  if (cachedEntries) {
    const exactMap = new Map();
    const canonicalMap = new Map();
    const allCandidates = [];
    for (const entry of cachedEntries) {
      const candidate = {
        url: entry.gifUrl,
        source: "exercisedb",
        title: entry.name
      };
      allCandidates.push(candidate);
      const exactKey = normalizeNameKey(entry.name);
      const canonicalKey = canonicalNameKey(entry.name);
      if (exactKey && !exactMap.has(exactKey)) {
        exactMap.set(exactKey, candidate);
      }
      if (canonicalKey) {
        const list = canonicalMap.get(canonicalKey) ?? [];
        list.push(candidate);
        canonicalMap.set(canonicalKey, list);
      }
    }
    return { exactMap, canonicalMap, allCandidates, entries: cachedEntries, fromCache: true };
  }

  const exactMap = new Map();
  const canonicalMap = new Map();
  const allCandidates = [];
  const entries = [];
  let after = "";
  let page = 0;

  while (true) {
    const url = new URL("https://oss.exercisedb.dev/api/v1/exercises");
    url.searchParams.set("limit", "25");
    if (after) {
      url.searchParams.set("after", after);
    }

    const payload = await fetchJsonWithRetry(url.toString(), 6, 20000);
    const list = Array.isArray(payload?.data) ? payload.data : [];
    page += 1;

    for (const item of list) {
      const name = typeof item?.name === "string" ? item.name.trim() : "";
      const gifUrl = typeof item?.gifUrl === "string" ? item.gifUrl.trim() : "";
      if (!name || !gifUrl || !isSafeRemoteMediaUrl(gifUrl)) continue;

      const candidate = {
        url: gifUrl,
        source: "exercisedb",
        title: name
      };
      allCandidates.push(candidate);
      entries.push({ name, gifUrl });

      const exactKey = normalizeNameKey(name);
      const canonicalKey = canonicalNameKey(name);
      if (exactKey && !exactMap.has(exactKey)) {
        exactMap.set(exactKey, candidate);
      }
      if (canonicalKey) {
        const listForKey = canonicalMap.get(canonicalKey) ?? [];
        listForKey.push(candidate);
        canonicalMap.set(canonicalKey, listForKey);
      }
    }

    console.log(`ExerciseDB page=${page}, items=${list.length}, entries=${entries.length}`);

    const nextCursor = typeof payload?.meta?.nextCursor === "string" ? payload.meta.nextCursor.trim() : "";
    const hasNext = Boolean(payload?.meta?.hasNextPage) && nextCursor;
    if (!hasNext || !nextCursor) {
      break;
    }
    after = nextCursor;
  }

  await fs.mkdir(path.dirname(exerciseDbIndexCachePath), { recursive: true });
  await fs.writeFile(
    exerciseDbIndexCachePath,
    JSON.stringify(
      {
        fetchedAt: new Date().toISOString(),
        entries
      },
      null,
      2
    ),
    "utf8"
  );

  return { exactMap, canonicalMap, allCandidates, entries, fromCache: false };
};

const chooseExerciseDbCandidate = (exerciseName, exactMap, canonicalMap) => {
  const exactKey = normalizeNameKey(exerciseName);
  if (exactKey && exactMap.has(exactKey)) {
    return {
      ...exactMap.get(exactKey),
      score: 1
    };
  }

  const canonicalKey = canonicalNameKey(exerciseName);
  const bucket = canonicalKey ? canonicalMap.get(canonicalKey) ?? [] : [];
  if (bucket.length === 0) {
    return null;
  }

  let best = null;
  for (const candidate of bucket) {
    const score = Math.max(
      nameSimilarityScore(exerciseName, candidate.title),
      canonicalSimilarityScore(exerciseName, candidate.title)
    );
    if (!best || score > best.score) {
      best = {
        ...candidate,
        score
      };
    }
  }

  if (!best) {
    return null;
  }
  if (!shouldAcceptExerciseDbMatch(exerciseName, best.title)) {
    return null;
  }
  return best;
};

const chooseExerciseDbCandidateFromAll = (exerciseName, allCandidates) => {
  let best = null;
  for (const candidate of allCandidates) {
    const score = Math.max(
      nameSimilarityScore(exerciseName, candidate.title),
      canonicalSimilarityScore(exerciseName, candidate.title)
    );
    if (!best || score > best.score) {
      best = {
        ...candidate,
        score
      };
    }
  }
  if (!best) {
    return null;
  }
  if (!shouldAcceptExerciseDbMatch(exerciseName, best.title)) {
    return null;
  }
  return best;
};

const buildWgerMatches = (localExercises, wgerMap) => {
  const rows = [];
  for (const exercise of localExercises) {
    const key = normalizeNameKey(exercise.name);
    if (!key) continue;
    const remote = wgerMap.get(key);
    if (!remote) continue;
    rows.push({
      exerciseId: exercise.id,
      exerciseName: exercise.name,
      videoUrl: remote.url,
      source: remote.source,
      sourceTitle: remote.title,
      matchScore: 1
    });
  }
  return rows;
};

const buildExerciseDbMatches = (localExercises, exerciseDbIndex) => {
  const rows = [];
  for (const exercise of localExercises) {
    const matchedByMap = chooseExerciseDbCandidate(
      exercise.name,
      exerciseDbIndex.exactMap,
      exerciseDbIndex.canonicalMap
    );
    const matched =
      matchedByMap ??
      chooseExerciseDbCandidateFromAll(exercise.name, exerciseDbIndex.allCandidates ?? []);
    if (!matched || !matched.url) continue;
    rows.push({
      exerciseId: exercise.id,
      exerciseName: exercise.name,
      videoUrl: matched.url,
      source: matched.source,
      sourceTitle: matched.title,
      matchScore: matched.score ?? 0
    });
  }
  return rows;
};

const collectAutoVideoFilesForExercise = async (exerciseId) => {
  const prefix = `video-auto-${exerciseId}-`;
  const files = await fs.readdir(mediaStorageRoot);
  return files.filter((fileName) => fileName.startsWith(prefix) && fileName.endsWith(".mp4"));
};

const applyDatabaseUpdates = async (rows) => {
  if (rows.length === 0) {
    return { stdout: "", stderr: "" };
  }
  const sqlStatements = ["SET NAMES utf8mb4;"];

  for (const row of rows) {
    sqlStatements.push(
      `UPDATE exercise_media
       SET is_deleted = 1,
           update_time = NOW(3)
       WHERE exercise_id = ${toSqlValue(row.exerciseId)}
         AND media_type = 'video'
         AND is_deleted = 0
         AND (
           url LIKE '/api/v1/exercise/media/file/video-auto-%'
           OR url LIKE '/api/v1/exercise/media/file/clip-%'
           OR url LIKE '%youtube.com%'
           OR url LIKE '%youtu.be%'
         );`
    );

    sqlStatements.push(
      `INSERT INTO exercise_media (
         exercise_id,
         media_type,
         url,
         cover_flag,
         create_time,
         update_time,
         is_deleted
       )
       SELECT
         ${toSqlValue(row.exerciseId)},
         'video',
         ${toSqlValue(row.videoUrl)},
         0,
         NOW(3),
         NOW(3),
         0
       FROM DUAL
       WHERE NOT EXISTS (
         SELECT 1
         FROM exercise_media m
         WHERE m.exercise_id = ${toSqlValue(row.exerciseId)}
           AND m.media_type = 'video'
           AND m.is_deleted = 0
           AND m.url = ${toSqlValue(row.videoUrl)}
       );`
    );
  }

  sqlStatements.push(
    "SELECT COUNT(*) AS remote_video_count FROM exercise_media WHERE is_deleted = 0 AND media_type = 'video' AND url LIKE 'http%';"
  );
  return runMysql(sqlStatements.join("\n\n"));
};

const removeLocalAutoFiles = async (rows) => {
  let removed = 0;
  for (const row of rows) {
    const fileNames = await collectAutoVideoFilesForExercise(row.exerciseId);
    for (const fileName of fileNames) {
      const filePath = path.resolve(mediaStorageRoot, fileName);
      try {
        await fs.rm(filePath, { force: true });
        removed += 1;
      } catch {
        // ignore file delete failures
      }
    }
  }
  return removed;
};

const summarizeBySource = (rows) => {
  const counter = new Map();
  for (const row of rows) {
    counter.set(row.source, (counter.get(row.source) ?? 0) + 1);
  }
  return [...counter.entries()]
    .map(([source, count]) => `${source}:${count}`)
    .join(", ");
};

